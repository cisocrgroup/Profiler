#include "GlobalHistPatterns.hpp"

////////////////////////////////////////////////////////////////////////////////
csl::GlobalHistPatterns csl::GlobalHistPatterns::INSTANCE;

////////////////////////////////////////////////////////////////////////////////
csl::GlobalHistPatterns &csl::GlobalHistPatterns::instance() {
  return INSTANCE;
}

////////////////////////////////////////////////////////////////////////////////
const std::vector<std::wstring> &
csl::GlobalHistPatterns::getHistPatterns() const {
  return patterns_;
}

////////////////////////////////////////////////////////////////////////////////
void csl::GlobalHistPatterns::addHistPattern(const std::wstring &pattern) {
  patterns_.push_back(pattern);
}
